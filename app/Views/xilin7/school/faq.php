            <h2 style='border:none'><a id="_Toc47440379"><span lang=EN>Q20:
Parent-On-Duty</span></a></h2>

            <p class=MsoNormal><span lang=EN style='color:black'>Parents are required to
serve as a parent-on-duty for at least one 2-hour and 20-minute time slot per
semester for each enrolled student. For example, if you have two children,
you need to do POD twice in a school semester. You can choose the date and time
when you register. Unfilled slots will be assigned by school to parents who
have not signed up. </span></p>

            <p class=MsoNormal><span lang=EN style='color:black'>&nbsp;</span></p>

            <p class=MsoNormal><span lang=EN style='color:black'>If parents fail to perform
POD duty during the current school year, there will be a $30 fine for each
missed slot during fall registration for the next school year. Parents can
choose to make a donation in the beginning of a school year to waive their POD
responsibilities. Parents who teach at Xilin NS are exempt from the
parent-on-duty service.&nbsp;</span><span lang=EN> </span></p>

