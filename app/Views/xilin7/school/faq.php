            <h1 style='border:none'><a id="_Toc47440357"><span lang=EN>Registration</span></a></h1>

            <h2 style='border:none'><a id="_Toc47440358"><span lang=EN>Q1: I am new to
the school and want to register my kid for a class. Where do I start?</span></a></h2>

            <p class=MsoNormal style='border:none'><span lang=EN>&nbsp;</span></p>

            <p class=MsoNormal style='border:none'><span lang=EN>New families can find the
school calendar and class schedule at our website: <a
                            href="http://www.xilinnschinese.org">www.xilinnschinese.org</a>. We offer
Chinese language classes for children for both heritage and non-heritage
speaker families. In addition, we offer a wide variety of enrichment classes in
art, culture, math, programming, and more. You can create a family account to
register for classes online and make payments via Zelle pay through your bank.
New students can try out the first two weeks of classes before registering for
the class. </span></p>

            <p class=MsoNormal style='border:none'><span lang=EN>&nbsp;</span></p>

            <p class=MsoNormal style='border:none'><span lang=EN>Please email the school
team at <a href="mailto:ec@xilinnschinese.org">ec@xilinnschinese.org</a> if you
need assistance. </span></p>

            <h2 style='border:none'><a id="_Toc47440359"><span lang=EN>Q2: Do I need to
go to school to register for classes in person?</span></a></h2>

            <p class=MsoNormal style='text-align:justify;border:none'><span lang=EN>No. You
can register for classes online as soon as registration is open for a semester.
We encourage early online registration to secure a seat before the class is
full. </span></p>

            <h2 style='border:none'><a id="_Toc47440360"><span lang=EN>Q3: Can I receive
email notifications when registration starts for next semester?</span></a></h2>

            <p class=MsoNormal style='text-align:justify;border:none'><span lang=EN>Our
website posts announcement on registration dates. New registration usually
starts on the last school day of the previous semester, usually around
mid-December for spring semester, and mid-May for fall semester. </span></p>

            <p class=MsoNormal style='text-align:justify;border:none'><span lang=EN>&nbsp;</span></p>

            <p class=MsoNormal style='text-align:justify;border:none'><span lang=EN>Once
you create a family account and register for any class, your email will be
added to our system for you to receive weekly messages from the principal. </span></p>

            <h2 style='border:none'><a id="_Toc47440361"><span lang=EN>Q4: What if I
forget my password?</span></a></h2>

            <p class=MsoNormal style='border:none'><span lang=EN>On the login page, you can
provide the email address in your family account and click on “Find Password”
to retrieve the information. It will be sent to the email you provided.</span></p>

            <p class=MsoNormal style='border:none'><span lang=EN>&nbsp;</span></p>

            <p class=MsoNormal style='border:none'><span lang=EN>For additional assistance,
please contact <a href="mailto:webmaster@xilinnschinese.org"><span
                                style='color:#1155CC'>webmaster@xilinnschinese.org</span></a> or talk to the
school team during the school day.</span></p>

            <h2 style='border:none'><a id="_Toc47440362"><span lang=EN>Q5: Can I get a
family discount?</span></a></h2>

            <p class=MsoNormal style='border:none'><span lang=EN>&nbsp;</span></p>

            <p class=MsoNormal style='text-align:justify;border:none'><span lang=EN>For
families with students registered in TWO or more Chinese classes, a 10%
discount will be applied to the family account for any <u>tuition</u> amount OTHER
THAN the Chinese classes. Material fee, book fee and registration fee are not
discountable. The online registration system calculates the discount
automatically and includes it in your invoice.</span></p>

            <p class=MsoNormal style='text-align:justify;border:none'><span lang=EN>&nbsp;</span></p>

            <h2 style='border:none'><a id="_Toc47440363"><span lang=EN>Q6: How do I pay
the tuition?</span></a></h2>

            <p class=MsoNormal style='border:none'><span lang=EN>&nbsp;</span></p>

            <p class=MsoNormal style='text-align:justify;border:none'><span lang=EN>Tuition
can be paid online from your bank using Zelle Pay at <a
                            href="mailto:payment@xilinnschinese.org">payment@xilinnschinese.org</a>. We do
NOT accept credit card. Onsite payment in Check or Cash is also accepted once
school starts. Please refer to the registration guidelines posted on our
website for details.</span></p>

            <h2 style='text-align:justify'><a id="_Toc47440364"></a><a
                        name="_24vxzo386252"></a><span lang=EN>Q7: Can I register for classes in the
middle of a semester?</span></h2>

            <p class=MsoNormal><span lang=EN>You can enroll your students in any open
classes throughout the semester and pay prorated tuition and fees.</span></p>

            <h2 style='text-align:justify'><a id="_Toc47440365"></a><a
                        name="_9inh7shmoaem"></a><span lang=EN>Q8: Can I drop classes?</span></h2>

            <p class=MsoNormal style='text-align:justify;border:none'><span lang=EN>&nbsp;</span></p>

            <p class=MsoNormal style='text-align:justify;border:none'><span lang=EN>You can
drop classes before the third week of class without penalty. We will refund
your full tuition and fees. In principle, we do not refund book fees after you
have picked up the textbooks. School calendar lists specific dates for each
semester.</span></p>

            <h1 style='border:none'><a id="_Toc47440366"><span lang=EN>Textbooks</span></a></h1>

            <h2 style='border:none'><a id="_Toc47440367"><span lang=EN>Q9: How do I find
out if I have ordered textbooks?</span></a></h2>

            <p class=MsoNormal style='border:none'><span lang=EN>When you register for the
class, you have the option to register with or without the textbook. You can
verify the information in your invoice after logging into your family account
online. </span></p>

            <h2 style='border:none'><a id="_Toc47440368"><span lang=EN>Q10: How can I
order books after registration?</span></a></h2>

            <p class=MsoNormal style='border:none'><span lang=EN>Registration is the only
procedure to order textbooks. If you need books after registration, you need unregister
the class “without textbook” and re-register choosing the option “with textbook”.
</span></p>

            <h2 style='border:none'><a id="_Toc47440369"><span lang=EN>Q11: Do I have to
order the textbook before the “Early Bird” deadline?</span></a></h2>

            <p class=MsoNormal style='text-align:justify;border:none'><span lang=EN>We
strongly recommend you register and order textbooks before this “Early Bird”
deadline. This not only means savings for families, but also provides
information for the school team to estimate the numbers for book orders. If you
order after the deadline, you may not receive textbook on the first day of
class. Usually the Early Bird deadline is 3 weeks before the first school day
and is clearly stated in the class schedule. </span></p>

            <h2 style='border:none'><a id="_Toc47440370"><span lang=EN>Q12: How can I
pick up my books?</span></a></h2>

            <p class=MsoNormal style='border:none'><span lang=EN>If you make the payment
online <u>before</u> the first school day, your payment information is captured
in the roster, and you can come directly to pick up the textbook. Online
payment <u>on</u> the first school day may not be captured in time, and you
will need to present evidence of payment.</span></p>

            <p class=MsoNormal style='border:none'><span lang=EN>&nbsp;</span></p>

            <p class=MsoNormal style='border:none'><span lang=EN>If you make the payment in-person
with check or cash on the first school day, you will receive a voucher for book
pickup. </span></p>

            <h2><a id="_Toc47440371"></a><a id="_rchf1a9dzsmn"></a><span lang=EN>Q13: Can
I just order the books without registering for any class?</span></h2>

            <p class=MsoNormal style='border:none'><span lang=EN>No. We only purchase
textbooks for students registered in our classes. </span></p>

            <h2 style='border:none'><a id="_Toc47440372"><span lang=EN>Q14: What will
happen if I can’t pick up my books on the first school day?</span></a></h2>

            <p class=MsoNormal style='border:none'><span lang=EN>You can pick up your
textbook the following week; however, it is possible that all books are
distributed, and you will need to wait for the next order to arrive. We suggest
you ask a friend to make payment and pick up the book on your behalf on the
first school day.  </span></p>

            <h2 style='border:none'><a id="_Toc47440373"><span lang=EN>Q15: When is the
cut off time for book order?</span></a></h2>

            <p class=MsoNormal align=center style='text-align:center;border:none'><b><span
                            lang=EN>&nbsp;</span></b></p>

            <p class=MsoNormal style='border:none'><span lang=EN>The cut off time is the
first school day. We will provide books for students if you order books <u>before
or on</u> the first school day. We will not accept new book orders after the
cutoff date and will only provide textbooks in our inventory. If no book is left,
we will provide the information for parents to make their order online. </span></p>

            <h2 style='border:none'><a id="_Toc47440374"></a><a id="_nuxrixmvuhc2"></a><span
                        lang=EN>Q16: Can I return the books?</span></h2>

            <p class=MsoNormal style='border:none'><span lang=EN>You cannot return
textbooks and we do not refund book fees. If you have a special situation,
please talk to the principal to see if your return can be accommodated. </span></p>

